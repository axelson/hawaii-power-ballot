export function sortContests(contests) {
  contests.sort(function(a, b) {
    const contestIdOrderA = getContestOrder(a.contest_id)
    const contestIdOrderB = getContestOrder(b.contest_id)
    if (contestIdOrderA < contestIdOrderB) {
      return -1
    }
    if (contestIdOrderA > contestIdOrderB) {
      return 1
    }

    return 0
  })
}
